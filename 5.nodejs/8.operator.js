// operator

let a = 10;
let b = 5;

console.log(a ** b); //밑, 지수

// and -끝까지 봄
console.log(1 && 0); // false
console.log(0 && 1); // false
console.log(2 && 3); // 3
console.log(3 && 2); // 2

// or - 처음 것부터 봄
console.log(1 || 0); // true
console.log(0 || 1); // true
console.log(2 || 3); // 2
console.log(3 || 2); // 3

